
public class Employeur {
	private String nom;
	private String adresse;
	private int nmrImmatriculation;
	private String référenceOrganisme;
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getAdresse() {
		return adresse;
	}
	public void setAdresse(String adresse) {
		this.adresse = adresse;
	}
	public int getNmrImmatriculation() {
		return nmrImmatriculation;
	}
	public void setNmrImmatriculation(int nmrImmatriculation) {
		this.nmrImmatriculation = nmrImmatriculation;
	}
	public String getRéférenceOrganisme() {
		return référenceOrganisme;
	}
	public void setRéférenceOrganisme(String référenceOrganisme) {
		this.référenceOrganisme = référenceOrganisme;
	}
	

}
